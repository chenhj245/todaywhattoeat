def classify_ingredient(name: str) -> str:
    """
    根据食材名称推断分类

    Args:
        name: 食材名称

    Returns:
        分类名称（蔬菜/肉类/蛋奶/水产/主食/调味品/水果/干货/冷冻/其他）
    """
    # 去除空格
    name = name.strip()

    # 精确匹配
    for category, ingredients in INGREDIENT_CATEGORIES.items():
        if name in ingredients:
            return category

    # 部分匹配（包含关系）
    for category, ingredients in INGREDIENT_CATEGORIES.items():
        for ing in ingredients:
            # 如果食材名包含词典中的词，或词典中的词包含食材名
            if ing in name or name in ing:
                return category

    # 关键词匹配（注意：冷冻要先于其他分类判断）
    if "冻" in name or "冰" in name:
        return "冷冻"
    if any(keyword in name for keyword in ["肉", "排骨", "猪", "牛", "羊", "鸡", "鸭"]):
        return "肉类"
    if any(keyword in name for keyword in ["鱼", "虾", "蟹", "贝", "蛤", "蚝"]):
        return "水产"
    if any(keyword in name for keyword in ["蛋", "奶", "豆腐"]):
        return "蛋奶"
    if any(keyword in name for keyword in ["米", "面", "粉"]):
        return "主食"
    if any(keyword in name for keyword in ["酱", "油", "盐", "糖", "醋", "料酒"]):
        return "调味品"
    if any(keyword in name for keyword in ["菜", "瓜", "椒", "葱", "蒜", "姜"]):
        return "蔬菜"
    if any(keyword in name for keyword in ["果", "莓", "桃", "梨", "橙", "柚"]):
        return "水果"

    # 默认返回"其他"
    return "其他"


def normalize_ingredient_name(name: str) -> str:
    """
    标准化食材名称（同义词转换）

    Args:
        name: 原始名称

    Returns:
        标准化后的名称
    """
    name = name.strip()
    return SYNONYMS.get(name, name)


def find_similar_ingredients(name: str, inventory: list) -> list:
    """
    在库存中查找相似食材（模糊匹配 + 同义词）

    Args:
        name: 要查找的食材名
        inventory: 库存列表，每项是包含 "name" 字段的字典

    Returns:
        匹配到的库存项列表
    """
    results = []
    normalized_name = normalize_ingredient_name(name)

    for item in inventory:
        item_name = item.get("name", "")
        normalized_item = normalize_ingredient_name(item_name)

        # 精确匹配
        if normalized_name == normalized_item:
            results.append(item)
            continue

        # 包含匹配
        if normalized_name in normalized_item or normalized_item in normalized_name:
            results.append(item)
            continue

    return results
